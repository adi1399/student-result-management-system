    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno=jTextField5.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/adityadb","root","Aditya@1999");
            
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
            if(rs.next()){
               jTextField1.setText(rs.getString(2));
               jTextField2.setText(rs.getString(4));
               jTextField3.setText(rs.getString(5));
               jTextField4.setText(rs.getString(3));
               jTextField6.setText(rs.getString(6));
               
               jTextField17.setText(rs.getString(8));
               jTextField18.setText(rs.getString(9));
               jTextField19.setText(rs.getString(10));
               jTextField20.setText(rs.getString(11));
               jTextField21.setText(rs.getString(12));
               
               int s1=Integer.parseInt(jTextField17.getText());
               int s2=Integer.parseInt(jTextField18.getText());
               int s3=Integer.parseInt(jTextField19.getText());
               int s4=Integer.parseInt(jTextField20.getText());
               int s5=Integer.parseInt(jTextField21.getText());
               
               int result=s1+s2+s3+s4+s5;
               jTextField24.setText(String.valueOf(result));
               
               if(s1<30||s2<30||s3<30||s4<30||s5<30){
                   jTextField25.setText("Fail");
                   jLabel17.setText("Better Luck Next Time!");
               }
               else{
                   jTextField25.setText("Pass");
                   jLabel17.setText("Congratulations!");
               }
            }
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

